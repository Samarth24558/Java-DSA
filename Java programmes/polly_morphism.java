class Arithmetic
{
    static void add(int num1,int num2)
    {
        System.out.println(num1+num2);
    }
    static void add(double num1,double num2)
    {
        System.out.println(num1+num2);
    }
}

public class polly_morphism
{
    public static void main(String[] args) 
    {
        Arithmetic.add(20, 20);
    }

    
}